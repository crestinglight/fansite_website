        <h1>Dressage</h1><br>
    
    <p>Dressage is a French term meaning “training,” and its purpose is to develop the horse’s natural athletic ability and willingness to work, and remain calm, supple and attentive to his rider. Competitive dressage entails nine levels of progression, each having its own set of tests, along with special tests for variations such as musical freestyle and multi-horse competitions. These competitions take place in a regulation size flat arena with licensed judges who determine the score of the performing horse and rider. Horse and rider put on a performance of pre-determined movements from memory, requiring rhythm, elegance, and proper posture in order to achieve a high score. There is no jumping, no obstacles, and no differing terrain. This is a competition of purely horse and rider synergy and confirmation.</p>
    
    <p>Dressage first appeared in the Olympics in 1912 in a "military test" style which would evolve into what we currently know as dressage, show jumping, and eventing. After the US Cavalry was disbanded in 1948, the focus of these military competitions shifted to a civilian sport, where it began gaining momentum. Through the decades, experts have determined several ideals for the sport, and now we train our dressage horses to get as close to those ideals as possible.</p>
    
