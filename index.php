        <h1>An English Overview</h1><br>
    
    <p>The art of English riding is a tradition that goes back hundreds of years. Originated in England, it was brought to America and was considered the <em>proper</em> way of riding. While the Western style was developed by the American cowboys in the wild west, English has always been a much more refined style. The saddle is smaller, with no horn, and the bridle is much simpler. This allows communication to flow between horse and rider with very little interruption. In the English style, the rider controls the horse's movements with only their legs and the reins. Posture is very important, along with dress, especially in competition.<br>
    <br>
        While there are many types of competitions within the English riding arena, this site will focus on the main three that are most popular: Dressage, Show Jumping, and Cross Country. While these competitions happen on an individual basis, they can also be grouped into a single competition called 3-day eventing.<br>
        <br>
        <a href="Dressage.html"><strong>Dressage</strong></a> is a competition that takes place in a flat arena, wherein horse and rider are expected to perform from memory a series of predetermined movements. While this sport is aesthetically beautiful, often appearing that the team is dancing, it is most importantly an expression of the most advanced form of horse training. In order to perform the movements required in the competition, horse and rider must be entirely in tune with one another's commands, body language, and rhythm.<br>
        <br>
        <a href="ShowJumping.html"><strong>Show Jumping</strong></a> is a jumping competition within a closed arena, in which horse and rider must finish a series of jumps in the correct order, without knocking over any poles. The rider who can clear the jumps in the fastest times wins the competition.<br>
        <br>
        <a href="CrossCountry.html"><strong>Cross Country</strong></a> is an intense competition that takes place outside on a long course. The horse and rider team must gallop as fast as they can through the course, clearing every jump in their path. The jumps are often unusual, not the regular multi-pole standard that is seen in show jumping. These jumps can consist of solid walls followed by ponds, or triangular wedge jumps wherein the horse will need to find the right spot to go over. This competition can be exhilarating, but also dangerous.<br>
        
